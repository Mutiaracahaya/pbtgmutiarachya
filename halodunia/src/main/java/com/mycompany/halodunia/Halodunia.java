public class Halodunia {

    public static void main(String[] args) {
        // Nama file: HalloDunia.java
// Program sederhana untuk mencetak informasi pengguna dengan karakter khusus

        // Mencetak informasi pengguna dengan karakter khusus
        System.out.println("Nama : Tuliskan Nama Anda\b");    // \b untuk backspace
        System.out.println("Gender : Tuliskan Gender Anda\n"); // \n untuk baris baru
        System.out.println("Alamat :\tTuliskan Alamat Anda");  // \t untuk tab
        System.out.println("Tanggal Lahir : Tuliskan Tanggal Lahir Anda\r"); // \r untuk carriage return
        System.out.println("\\*Karakter Khusus\\*");  // \\ untuk mencetak backslash dan \* untuk bintang
        System.out.println("Nama Lengkap: \'Andika\'");  // \' untuk mencetak tanda petik tunggal
        System.out.println("Path: C:\\Users\\NamaAnda\\");  // \\ untuk mencetak tanda backslash
    }
}

    

